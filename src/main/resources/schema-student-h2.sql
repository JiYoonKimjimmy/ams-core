CREATE TABLE STUDENTS (
    ID BIGINT GENERATED BY DEFAULT AS IDENTITY,
    NAME VARCHAR(20),
    MOBILE_NUMBER VARCHAR(11),
    DATE_OF_BIRTH VARCHAR(8),
    GENDER CHAR(2),
    SCHOOL VARCHAR(20),
    GRADE CHAR(2),
    STATUS VARCHAR(10),
    CREATED DATETIME,
    UPDATED DATETIME,
    PRIMARY KEY (ID)
);